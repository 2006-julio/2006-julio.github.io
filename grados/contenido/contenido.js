//convertidor YouTube
function convertirYoutube(url) {

    if (url.includes("youtu.be")) {

        const id = url.split("/").pop();

        return `https://www.youtube.com/embed/${id}`;
    }

    if (url.includes("watch?v=")) {

        const id = url.split("v=")[1].split("&")[0];

        return `https://www.youtube.com/embed/${id}`;
    }

    return url;
}


